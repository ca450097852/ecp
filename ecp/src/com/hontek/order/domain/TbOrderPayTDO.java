public class TbOrderPayTDO {

	//需要支付总额
	private String totalAcount ;
	
	//支付的订单Id
	private List<Integer> orderIdList ;
	
	//支付订单数量
	private Integer total ;

	public TbOrderPayTDO() {
		super();
	}

	public TbOrderPayTDO(String totalAcount, List<Integer> orderIdList,Integer total) {
		super();
		this.totalAcount = totalAcount;
		this.orderIdList = orderIdList;
		this.total = total ;
	}

	public String getTotalAcount() {
		return totalAcount;
	}

	public void setTotalAcount(String totalAcount) {
		this.totalAcount = totalAcount;
	}

	public List<Integer> getOrderIdList() {
		return orderIdList;
	}

	public void setOrderIdList(List<Integer> orderIdList) {
		this.orderIdList = orderIdList;
	}

	public Integer getTotal() {
		return total;
	}

	public void setTotal(Integer total) {
		this.total = total;
	}
	
	
}
