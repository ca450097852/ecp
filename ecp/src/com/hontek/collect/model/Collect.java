package com.hontek.collect.model;
/**
 * 收藏表
 * @author jsb
 *
 */
public class Collect {

	private Integer collectId;
	private Integer menberId;
	private Integer goodsId;
	private Integer shopId;
	private String createTime;
	private String collectType;
	
	//商品信息
	private String goodsName;
	private String goodsImg;
	private Float  saledPrice;
	
	//店铺信息
	private String shopName;
	private String shopLog;
	private String shopDesc;
	public Integer getCollectId() {
		return collectId;
	}
	public void setCollectId(Integer collectId) {
		this.collectId = collectId;
	}
	public Integer getMenberId() {
		return menberId;
	}
	public void setMenberId(Integer menberId) {
		this.menberId = menberId;
	}
	public Integer getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(Integer goodsId) {
		this.goodsId = goodsId;
	}
	public Integer getShopId() {
		return shopId;
	}
	public void setShopId(Integer shopId) {
		this.shopId = shopId;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getCollectType() {
		return collectType;
	}
	public void setCollectType(String collectType) {
		this.collectType = collectType;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getGoodsImg() {
		return goodsImg;
	}
	public void setGoodsImg(String goodsImg) {
		this.goodsImg = goodsImg;
	}
	public Float getSaledPrice() {
		return saledPrice;
	}
	public void setSaledPrice(Float saledPrice) {
		this.saledPrice = saledPrice;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getShopLog() {
		return shopLog;
	}
	public void setShopLog(String shopLog) {
		this.shopLog = shopLog;
	}
	public String getShopDesc() {
		return shopDesc;
	}
	public void setShopDesc(String shopDesc) {
		this.shopDesc = shopDesc;
	}
	
}
